import math
fio = input("Введите ФИО: ")
print(fio + " начал изучать Питон!")
print("---------------")
x = int(input("Введите целое число х: "))
x_add = str(x + x)
x_razn = str(x - x)
x_slojno = str(x / (x + x))
print("Если провести вычисление х+х, то получится результат: " + x_add)
print("Если провести вычисление х-х, то получится результат: " + x_razn)
print("Если провести вычисление х/(x+x), то получится результат: " + x_slojno)
print("---------------")
y = float(input("Введите вещественное число y: "))
y_slojno_very = str((y + y) / (y * y))
print("Если провести вычисление (y + y) / (y * y), то получится результат: " + y_slojno_very)
print("Результат с 2мя знаками после запятой: " + "{0:.2f}".format((y + y) / (y * y)))
print("---------------")
